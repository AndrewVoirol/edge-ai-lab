#!/bin/bash
# Generate a benchmark dashboard markdown file from metrics/history.json
# Usage: ./.antigravity/skills/benchmark-comparison/scripts/generate-dashboard.sh [output_file]
#
# Produces a standalone markdown dashboard suitable for review.
# If output_file is provided, writes to that file. Otherwise writes to stdout.

set -euo pipefail

WORKSPACE="$(cd "$(dirname "$0")/../../../.." && pwd)"
METRICS_FILE="$WORKSPACE/metrics/history.json"
OUTPUT="${1:-}"

if [ ! -f "$METRICS_FILE" ]; then
  echo "❌ No metrics/history.json found" >&2
  exit 1
fi

generate() {
  local total
  total=$(jq 'length' "$METRICS_FILE")
  local latest_ts
  latest_ts=$(jq -r 'last.timestamp // "N/A"' "$METRICS_FILE")

  cat <<EOF
# 📊 Benchmark Dashboard

**Last updated:** $latest_ts
**Total metric entries:** $total
**Generated:** $(date -u +"%Y-%m-%dT%H:%M:%SZ")

---

## Latest Benchmark Results

EOF

  # Get the most recent entry with benchmarks
  jq -r '
    [.[] | select(.benchmarks | length > 0)] | last //empty |
    "**Session:** \(.sessionId | .[0:8])...\n**Platform:** \(.platform)\n**Timestamp:** \(.timestamp)\n**Tests:** \(.testResults.passed) passed, \(.testResults.failed) failed, \(.testResults.skipped) skipped\n"
  ' "$METRICS_FILE" 2>/dev/null || echo "No benchmark data available yet."

  echo ""
  echo "| Model/Config | Decode (tok/s) | TTFT (s) | Init (ms) | Tokens |"
  echo "|---|---|---|---|---|"

  jq -r '
    [.[] | select(.benchmarks | length > 0)] | last // empty |
    (.benchmarks // [])[] |
    "| \(.label) | **\(.decodeSpeed | . * 10 | round / 10)** | \(.ttft | . * 100 | round / 100) | \(.initTimeMs | round) | \(.avgTokens) |"
  ' "$METRICS_FILE" 2>/dev/null || echo "| — | — | — | — | — |"

  cat <<'EOF'

---

## Gallery Comparison (iPhone 16 Pro Max, v1.0.6)

| Model | Gallery Decode | Our Best Decode | Delta |
|---|---|---|---|
EOF

  # Calculate deltas if we have data
  local our_g4_decode our_g3n_decode
  our_g4_decode=$(jq -r '[.[] | .benchmarks[]? | select(.label | contains("Standard/GPU")) | .decodeSpeed] | last // 0' "$METRICS_FILE" 2>/dev/null)
  our_g3n_decode=$(jq -r '[.[] | .benchmarks[]? | select(.label | contains("Gemma3n")) | .decodeSpeed] | last // 0' "$METRICS_FILE" 2>/dev/null)

  if [ "$our_g4_decode" != "0" ] && [ "$our_g4_decode" != "null" ]; then
    printf "| Gemma 4 E2B | 41.65 | **%.1f** | %.1f%% |\n" "$our_g4_decode" "$(echo "scale=1; ($our_g4_decode - 41.65) / 41.65 * 100" | bc 2>/dev/null || echo "N/A")"
  else
    echo "| Gemma 4 E2B | 41.65 | — | — |"
  fi

  if [ "$our_g3n_decode" != "0" ] && [ "$our_g3n_decode" != "null" ]; then
    printf "| Gemma 3n E2B | 25.57 | **%.1f** | %.1f%% |\n" "$our_g3n_decode" "$(echo "scale=1; ($our_g3n_decode - 25.57) / 25.57 * 100" | bc 2>/dev/null || echo "N/A")"
  else
    echo "| Gemma 3n E2B | 25.57 | — | — |"
  fi

  cat <<'EOF'

> [!NOTE]
> Gallery uses MTP (speculative decoding) + topK=1 (greedy) on iOS device.
> Our numbers above are macOS, no MTP, topK=64. Direct comparison requires iOS device run.

---

## Historical Trend

EOF

  echo "| Timestamp | Platform | Label | Decode (tok/s) |"
  echo "|---|---|---|---|"

  jq -r '
    .[] | select(.benchmarks | length > 0) |
    .timestamp as $ts | .platform as $plat |
    (.benchmarks // [])[] |
    "| \($ts | .[0:16]) | \($plat) | \(.label) | \(.decodeSpeed | . * 10 | round / 10) |"
  ' "$METRICS_FILE" 2>/dev/null || echo "| — | — | — | — |"

  cat <<'EOF'

---

## Known Issues

| # | Issue | Impact on Benchmarks |
|---|---|---|
| 4 | BenchmarkInfo nil w/ MTP | Prefill/decode from SDK unavailable; use wall-clock |
| 7 | BenchmarkInfo nil without MTP (macOS) | All metrics are wall-clock only |
| 3 | Gallery CPU returns all zeros | CPU comparison impossible for GPU-only models |

---

*Dashboard generated by `.antigravity/skills/benchmark-comparison/scripts/generate-dashboard.sh`*
EOF
}

if [ -n "$OUTPUT" ]; then
  generate > "$OUTPUT"
  echo "📊 Dashboard written to $OUTPUT" >&2
else
  generate
fi
