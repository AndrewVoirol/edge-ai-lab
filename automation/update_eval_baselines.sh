# update_eval_baselines.sh
#
# Promotes the latest eval pipeline run results to new baselines.
# Use this after intentional model or prompt changes that shift eval scores.
#
# Usage:
#   ./automation/update_eval_baselines.sh
#
# Prerequisites:
#   - metrics/eval_history.json must contain at least one run
#   - jq must be installed (brew install jq)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(dirname "$SCRIPT_DIR")"

EVAL_HISTORY="$PROJECT_DIR/metrics/eval_history.json"
EVAL_BASELINES="$PROJECT_DIR/metrics/eval_baselines.json"

# Check prerequisites
if ! command -v jq &> /dev/null; then
    echo "❌ jq is required but not installed. Install with: brew install jq"
    exit 1
fi

if [ ! -f "$EVAL_HISTORY" ]; then
    echo "❌ eval_history.json not found at: $EVAL_HISTORY"
    echo "   Run the eval pipeline first: -RunEvalPipeline"
    exit 1
fi

# Get the latest run
LATEST_RUN=$(jq '.runs[-1]' "$EVAL_HISTORY")

if [ "$LATEST_RUN" = "null" ] || [ -z "$LATEST_RUN" ]; then
    echo "❌ No runs found in eval_history.json"
    echo "   Run the eval pipeline first: -RunEvalPipeline"
    exit 1
fi

TIMESTAMP=$(echo "$LATEST_RUN" | jq -r '.timestamp')
MODEL=$(echo "$LATEST_RUN" | jq -r '.model')

echo "═══════════════════════════════════════════"
echo "📊 Promoting Latest Eval Run to Baselines"
echo "═══════════════════════════════════════════"
echo ""
echo "  Timestamp: $TIMESTAMP"
echo "  Model:     $MODEL"
echo ""

# Build new baselines from the latest run
NEW_BASELINES=$(echo "$LATEST_RUN" | jq --arg model "$MODEL" --arg ts "$TIMESTAMP" '{
  "_meta": {
    "version": 1,
    "description": "Eval suite baselines for regression detection. Auto-generated by update_eval_baselines.sh.",
    "last_updated": $ts,
    "default_threshold_pct": 10,
    "methodology": "Pass rates from -RunEvalPipeline, greedy sampler (topK=1), GPU backend."
  },
  "baselines": [.suites[] | select(.pass_rate != null and .status != "skipped") | {
    "suite": .name,
    "model": $model,
    "baseline_pass_rate": .pass_rate,
    "min_pass_rate": ((.pass_rate - 0.15) | if . < 0 then 0 else . end),
    "prompt_count": (.prompt_count // 0),
    "source": ("update_eval_baselines.sh — " + $ts),
    "notes": null
  }],
  "regression_rules": {
    "pass_rate": {
      "direction": "higher_is_better",
      "threshold_pct": 10,
      "severity": "critical",
      "description": "Overall pass rate — primary eval quality metric"
    },
    "math_reasoning_rate": {
      "direction": "higher_is_better",
      "threshold_pct": 15,
      "severity": "critical",
      "description": "Math and logical reasoning pass rate"
    },
    "instruction_following_rate": {
      "direction": "higher_is_better",
      "threshold_pct": 10,
      "severity": "warning",
      "description": "Instruction following and format compliance"
    },
    "knowledge_rate": {
      "direction": "higher_is_better",
      "threshold_pct": 20,
      "severity": "info",
      "description": "General knowledge and factuality"
    }
  }
}')

echo "  New baselines:"
echo "$NEW_BASELINES" | jq -r '.baselines[] | "    • \(.suite): \(.baseline_pass_rate * 100 | floor)% (min floor: \(.min_pass_rate * 100 | floor)%)"'
echo ""

# Backup existing baselines
if [ -f "$EVAL_BASELINES" ]; then
    BACKUP="$EVAL_BASELINES.bak.$(date +%Y%m%d_%H%M%S)"
    cp "$EVAL_BASELINES" "$BACKUP"
    echo "  💾 Backed up existing baselines to: $(basename "$BACKUP")"
fi

# Write new baselines
echo "$NEW_BASELINES" | jq '.' > "$EVAL_BASELINES"
echo "  ✅ Updated eval_baselines.json"
echo ""
echo "═══════════════════════════════════════════"
echo "  Done! Review and commit the changes:"
echo "    git add metrics/eval_baselines.json"
echo "    git commit -m 'chore: update eval baselines'"
echo "═══════════════════════════════════════════"
